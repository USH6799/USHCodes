n = int(input("Enter the value of n : "))

# ----------------------------------------------------------
# Using For loop:
# ----------------------------------------------------------

for i in range(n , 0 , -1): # Here, the value of var 'i' goes from n to 1 and it gives the output as True and the for loop is executed.

    print(i) # Output: 9 8 7 6 5 4 3 2 1
    

# ----------------------------------------------------------
# Using While loop:
# ----------------------------------------------------------

i = n # Here, the value of var 'i' is n and it gives the output as True and the while loop is executed.

while i > 0: # Here, the value of var 'i' is greater than 0 and it gives the output as True and the while loop is executed.
    
    print(i) # Output: 9 8 7 6 5 4 3 2 1
    
    i = i - 1 # Here, the value of var 'i' is decreased by 1 and it gives the output as True and the while loop is executed.

