public class Concatenation {
    // classname Concatenation

    public static void main(String[] args) {
        // main function to run the code.

        String str1 = "First half and";
        // 1st String variable for storing the first part of the sentence.
        // Data type: String
        
        String str2 = " Second half.";
        // 2nd String variable for storing the second part of the sentence.
        // Data type: String

        System.out.println(str1 + str2);
        // Printing the result of concatenating str1 and str2.
        // The '+' operator is used to concatenate the two strings.
    }
}

